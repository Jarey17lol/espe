--Ejercicio 12 precio promedio de los productos

SELECT AVG(precio) AS precio_promedio
FROM productos;

--AVG(precio) Función agregada que calcula el promedio de los valores en la columna precio.
--AS precio_promedio Asigna un nombre al resultado obtenido.
--FROM productos Indica que los datos se toman de la tabla productos.
--Permite conocer la media aritmética de todos los precios registrados.